package com.variable.assign;

public class Shift {

	public static void main(String[] args) {
		//signed right shift
		//add the needed zeros at the left
		int a=8;
		int b=a>>2;
		System.out.println(b);
		int c=2;
		int d=c>>2;
		System.out.println(d);
		int e=1;
		int f=e>>2;
		System.out.println(f);
		//signed left shift
		//add the neede zeros at the left
		int g=-8;
		int h=g<<2;
		System.out.println(h);
		int i=-2;
		int j=i<<2;
		System.out.println(j);
		int k=-1;
		int l=k<<2;
		System.out.println(l);
		
		
	
	
	
	}

}
